<script setup>
import { ref, reactive, onMounted, onBeforeUnmount, watch } from 'vue';

const view = ref('landing');
const isGenerating = ref(false);
const generatedPdfBlob = ref(null);
const particleCanvas = ref(null);

// ── Loader state ──
const loaderStep = ref(0);
const loaderProgress = ref(0);
const loaderSteps = [
  'Parsing your career data',
  'Expanding with AI metrics',
  'Applying Harvard formatting',
  'Injecting ATS keywords',
  'Generating PDF',
];
const loaderPhases = ['Analyzing...', 'Expanding...', 'Formatting...', 'Optimizing...', 'Compiling...'];
const loaderPhase = ref(loaderPhases[0]);
let loaderInterval = null;

watch(view, (v) => {
  if (v === 'generating') startLoaderAnimation();
  else stopLoaderAnimation();
});

function startLoaderAnimation() {
  stopLoaderAnimation(); // clear any existing interval first
  loaderStep.value = 0;
  loaderProgress.value = 0;
  loaderPhase.value = loaderPhases[0];
  let step = 0;
  loaderInterval = setInterval(() => {
    if (step < loaderSteps.length - 1) {
      step++;
      loaderStep.value = step;
      loaderPhase.value = loaderPhases[step] || loaderPhases[loaderPhases.length - 1];
      loaderProgress.value = Math.round((step / (loaderSteps.length - 1)) * 80); // cap at 80% — API call fills the rest
    }
  }, 1400);
}

function stopLoaderAnimation() {
  if (loaderInterval) { clearInterval(loaderInterval); loaderInterval = null; }
}

// ── Form data ──
const formData = reactive({
  name: '', email: '', phone: '', links: '', summary: '', skills: '',
  experience: [{ role: '', company: '', duration: '', description: '' }],
  projects: [{ name: '', tech: '', description: '' }],
  education: [{ degree: '', school: '', year: '' }],
  target_company: ''
});

