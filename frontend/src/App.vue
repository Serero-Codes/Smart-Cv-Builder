const submitForm = async () => {
  view.value = 'generating';
  isGenerating.value = true;
  try {
    const response = await fetch('http://localhost:5000/api/generate/form', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(formData)
    });
    if (!response.ok) {
      const err = await response.json();
      throw new Error(err.error || "Server Error");
    }
    generatedPdfBlob.value = await response.blob();
    loaderProgress.value = 100;
    loaderStep.value = loaderSteps.length;
    setTimeout(() => { view.value = 'done'; }, 600);
  } catch (error) {
    console.error(error);
    alert(`Error: ${error.message}`);
    view.value = 'form';
  } finally {
    isGenerating.value = false;
  }
};

const downloadFile = () => {
  if (!generatedPdfBlob.value) return;
  const url = window.URL.createObjectURL(generatedPdfBlob.value);
  const a = document.createElement('a');
  a.href = url;
  a.download = `${formData.name.replace(/\s+/g, '_')}_CV.pdf`;
  document.body.appendChild(a);
  a.click();
  a.remove();
  window.URL.revokeObjectURL(url);
};

// ── Particle system ──
let animFrame = null;
let particles = [];
let resizeHandler = null;

function initParticles() {
  const canvas = particleCanvas.value;
  if (!canvas) return;
  const ctx = canvas.getContext('2d');
  resizeHandler = () => {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
  };
  resizeHandler();
  window.addEventListener('resize', resizeHandler);

  const count = Math.min(60, Math.floor(window.innerWidth / 20));
  particles = Array.from({ length: count }, () => ({
    x: Math.random() * canvas.width,
    y: Math.random() * canvas.height,
    vx: (Math.random() - 0.5) * 0.3,
    vy: (Math.random() - 0.5) * 0.3,
    r: Math.random() * 1.5 + 0.5,
    a: Math.random() * 0.5 + 0.1,
  }));

  const draw = () => {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    particles.forEach(p => {
      p.x += p.vx; p.y += p.vy;
      if (p.x < 0) p.x = canvas.width;
      if (p.x > canvas.width) p.x = 0;
      if (p.y < 0) p.y = canvas.height;
      if (p.y > canvas.height) p.y = 0;
      ctx.beginPath();
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fillStyle = `rgba(255,255,255,${p.a})`;
      ctx.fill();
    });
    // Draw connecting lines
    for (let i = 0; i < particles.length; i++) {
      for (let j = i + 1; j < particles.length; j++) {
        const dx = particles[i].x - particles[j].x;
        const dy = particles[i].y - particles[j].y;
        const dist = Math.sqrt(dx * dx + dy * dy);
        if (dist < 120) {
          ctx.beginPath();
          ctx.moveTo(particles[i].x, particles[i].y);
          ctx.lineTo(particles[j].x, particles[j].y);
          ctx.strokeStyle = `rgba(255,255,255,${0.08 * (1 - dist / 120)})`;
          ctx.lineWidth = 0.5;
          ctx.stroke();
        }
      }
    }
    animFrame = requestAnimationFrame(draw);
  };
  draw();
}

onMounted(() => { initParticles(); });
onBeforeUnmount(() => {
  if (animFrame) cancelAnimationFrame(animFrame);
  if (resizeHandler) window.removeEventListener('resize', resizeHandler);
  stopLoaderAnimation();
});
</script>

